import { mapState } from "vuex";
import {
    getDoctorMessage1,
    drugsByCondition,
    addPrescription
} from "../../api/apiAll.js";
export default {
    components: {
        search
    },
    data() {
        return {
            form: {
                name: ""
            },
            familyMessage: {
                name: "",
                age: "",
                sex: "",
                birthday: "",
                phone: "",
                address: ""
            },
            drugsData: [],
            chufangData: {
                id: "", //空
                clinicId: "",
                departmentId: "",
                userId: "",
                firstDoctorId: "",
                secondDoctorId: "", //空
                reviewEnum: null, //空
                orgCode: "",
                complained: "",
                medicalHistory:
                    "",
                allergyHistory: "",
                diagnosis: "",
                report: true,
                review: false,
                occurTime: "",
                reviewTime: "",
                remark: "",
                drugDetails: [
                    {
                        id: "H33020485", //药品id
                        drugPrice: 30.0, //药品价格
                        drugQuantity: 2, //药品数量
                        subtotal: 60.0, //药品🐤小计
                        doctorAsk: "一定要按时按量吃药" //医生嘱托
                    }
                ]
            }
        };
    },
    computed: {
        ...mapState({
            userState: state => state.user.userInfo,
            userSelfInfo: state => state.user.userSelfInfo
        })
    },
    methods: {
        //获取处方信息
        async getDrugsMessage() {
            let _this = this;
            let query = {
                token: this.userState.token,
                familyMemberId: this.sendToUserId
            };
            const res = await getDoctorMessage1(query);

            if (res.data && res.data.errCode === 0) {
                _this.familyMessage = {
                    name: res.data.body.name,
                    age: res.data.body.age,
                    sex: res.data.body.sex,
                    birthday: res.data.body.birthday,
                    phone: res.data.body.phone,
                    address: res.data.body.address
                };
            } else {
                //失败
                this.$notify.error({
                    title: "警告",
                    message: res.data.errMsg
                });
            }
        },
        //获取处方表格信息
        async getDrugsByCondition() {
            let query = {
                token: this.userState.token,
                drugName: ""
            };
            const res = await drugsByCondition(query);
            if (res.data && res.data.errCode === 0) {
                // console.log()
                this.chufangData.drugDetails = res.data.body;
            } else {
                //失败
                this.$notify.error({
                    title: "警告",
                    message: res.data.errMsg
                });
            }
        },
        //提交审核
        async submitAudit() {
            let query = {
                token: this.userState.token
            };
            let options = this.chufangData;
            const res = await addPrescription(query, options);
            if (res.data && res.data.errCode === 0) {
                this.$notify.success({
                    title: "成功",
                    message: "审核成功"
                });
            } else {
                //失败
                this.$notify.error({
                    title: "警告",
                    message: res.data.errMsg
                });
            }
        },
        setMessage(){
            this.chufangData.firstDoctorId=this.userSelfInfo.userId;
            this.chufangData.clinicId=this.userMessage.clinicId
            this.chufangData.departmentId=this.userMessage.departmentId
            this.chufangData.userId=this.userMessage.userId
            this.chufangData.orgCode=this.userMessage.orgCode
        }
    },
    props: {
        sendToUserId: String,
        userMessage:Object
    },
    model: {
        prop: ["sendToUserId","userMessage"],
        event: "reBack"
    },
    created() {
        this.getDrugsMessage();
        this.getDrugsByCondition();
        this.setMessage()
    },
    beforeDestroy() {}
};
</script>

